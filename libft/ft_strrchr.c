#include <stdio.h>

char	*ft_strrchr(char *s, int c)
{
	char	*rez;

	rez = NULL;
	while (*s)
	{
		if (*s == c)
			rez = s;
		s++;
	}
	if (c == 0)
		return (s);
	return (rez);
}
