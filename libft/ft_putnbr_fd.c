#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && n > -10)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n / 10 != 0)
		ft_putnbr_fd(n / 10, fd);
	if (n < 0)
		ft_putchar_fd('0' + n % 10 * -1, fd);
	else
		ft_putchar_fd('0' + n % 10, fd);
}
