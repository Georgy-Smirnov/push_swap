#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int				i;
	unsigned char	*a;

	i = 0;
	a = (unsigned char *)b;
	while (len)
	{
		a[i] = c;
		i++;
		len--;
	}
	return (a);
}
